/** Linear scan; returns null when the closest city is farther than 300km. */
export function nearestCity(lat: number, lon: number): City | null {
  let best: City | null = null;
  let bestDist = Infinity;
  for (const city of CITIES) {
    const dist = haversineM({ lat, lon }, city);
    if (dist < bestDist) {
      best = city;
      bestDist = dist;
    }
  }
  if (best === null || bestDist > NEAREST_CITY_MAX_M) return null;
  return best;
}
